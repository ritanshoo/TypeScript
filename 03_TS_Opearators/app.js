// arithmetic operators , logical operators , conditional operators
// Arithmetic
var num1 = 10;
var num2 = 20;
console.log("Sum : " + (num1 + num2));
console.log("sub : " + (num1 - num2));
console.log("Mul : " + num1 * num2);
